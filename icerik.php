    <h2>What is pen test?</h2>
    <p>Penetration testing (also called pen testing) is the practice of testing a computer system, network or Web
        application to find vulnerabilities that an attacker could exploit.</p><br>
    <p>Pen tests can be automated with software applications or they can be performed manually. Either way, the process
        includes gathering information about the target before the test (reconnaissance), identifying possible entry
        points, attempting to break in (either virtually or for real) and reporting back the findings.

        The main objective of penetration testing is to determine security weaknesses. A pen test can also be used to
        test an organization's security policy compliance, its employees' security awareness and the organization's
        ability to identify and respond to security incidents.

        Penetration tests are sometimes called white hat attacks because in a pen test, the good guys are attempting to
        break in.

        Pen test strategies include:</p>
    <div class="panel-group" id="accordion">
        <div class="panel panel-default">
            <div class="panel-heading">
                <h4 class="panel-title">
                    <a data-toggle="collapse" data-parent="#accordion" href="#collapse1">Targeted testing
                    </a>
                </h4>
            </div>
            <div id="collapse1" class="panel-collapse collapse in">
                <div class="panel-body">Targeted testing is performed by the organization's IT team and the penetration
                    testing team working together. It's sometimes referred to as a "lights-turned-on" approach because
                    everyone can see the test being carried out.
                </div>
            </div>
        </div>
        <div class="panel panel-default">
            <div class="panel-heading">
                <h4 class="panel-title">
                    <a data-toggle="collapse" data-parent="#accordion" href="#collapse2">External testing
                    </a>
                </h4>
            </div>
            <div id="collapse2" class="panel-collapse collapse">
                <div class="panel-body">This type of pen test targets a company's externally visible servers or devices
                    including domain name servers (DNS), e-mail servers, Web servers or firewalls. The objective is to
                    find out if an outside attacker can get in and how far they can get in once they've gained access.
                </div>
            </div>
        </div>
        <div class="panel panel-default">
            <div class="panel-heading">
                <h4 class="panel-title">
                    <a data-toggle="collapse" data-parent="#accordion" href="#collapse3">Internal testing
                    </a>
                </h4>
            </div>
            <div id="collapse3" class="panel-collapse collapse">
                <div class="panel-body">This test mimics an inside attack behind the firewall by an authorized user with
                    standard access privileges. This kind of test is useful for estimating how much damage a disgruntled
                    employee could cause.
                </div>
            </div>
        </div>
        <div class="panel panel-default">
            <div class="panel-heading">
                <h4 class="panel-title">
                    <a data-toggle="collapse" data-parent="#accordion" href="#collapse4">Blind testing
                    </a>
                </h4>
            </div>
            <div id="collapse4" class="panel-collapse collapse">
                <div class="panel-body">A blind test strategy simulates the actions and procedures of a real attacker by
                    severely limiting the information given to the person or team that's performing the test beforehand.
                    Typically, they may only be given the name of the company. Because this type of test can require a
                    considerable amount of time for reconnaissance, it can be expensive.
                </div>
            </div>
        </div>
        <div class="panel panel-default">
            <div class="panel-heading">
                <h4 class="panel-title">
                    <a data-toggle="collapse" data-parent="#accordion" href="#collapse5">Double blind testing

                    </a>
                </h4>
            </div>
            <div id="collapse5" class="panel-collapse collapse">
                <div class="panel-body">Double blind testing takes the blind test and carries it a step further. In this
                    type of pen test, only one or two people within the organization might be aware a test is being
                    conducted. Double-blind tests can be useful for testing an organization's security monitoring and
                    incident identification as well as its response procedures.
                </div>
            </div>
        </div>
    </div>
